#Prompting user for input on the day of the week as a number between 0-6
day = int(input('Day (0-6)? '))
if (day==0):#utilizing conditional statements to display the day of the week
    print('Sunday')
elif (day==1):
    print('Monday')
elif (day==2):
    print('Tuesday')
elif (day==3):
    print('Wednesday')
elif (day==4):
    print('Thursday')
elif (day==5):
    print('Friday')
elif (day==6):
    print('Saturday')

